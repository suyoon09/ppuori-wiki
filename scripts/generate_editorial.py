def main():
    with open(DATA, 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    updated = 0
    for item in data:
        table = get_food_table(item)
        # Filter out empty entries
        table = [r for r in table if r['food'] and r['amount']]
        
        if table:
            item['food_sources_table'] = table
            updated += 1
    
    with open(DATA, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    # Audit
    has_table = sum(1 for x in data if x.get('food_sources_table'))
    has_specific = sum(1 for x in data if x['id'] in FOOD_TABLES)
    print(f'Updated: {updated}/{len(data)}')
    print(f'With specific food tables: {has_specific}')
    print(f'With any table: {has_table}')
    print(f'Without table: {len(data) - has_table}')
    
    # Show sample
    for item in data:
        if item['id'] == 'vitamin-b1':
            print(f'\nSample vitamin-b1 table:')
            for r in item['food_sources_table']:
                print(f'  {r["food"]}: {r["amount"]}')
            break

if __name__ == '__main__':
    main()
